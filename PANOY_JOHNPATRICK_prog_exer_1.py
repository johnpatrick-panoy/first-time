first = float( input("Enter first number: "))
second = float( input("Enter second number: "))
sum = first + second 
print("Addition result:" + str(sum))
sub = first - second 
print("Subtraction result:"+ str(sub))
mul = first * second
print("Multiplication result:"+ str(mul))
div = first / second
print("Division result:"+ str(div))
print("Equal result:" + str(first==second))
print("Greater result:" + str(first>second))
print("less  or equal result:" + str(first>=second))
print("Bitwise AND result:" + str(0 & 0))
print("Bitwise OR result:" + str(4|3))
print("Logical AND result:" + str(first<4.6 and second<3.8))
print("Logical OR result:" + str(first==4.6 or second==3.8))
